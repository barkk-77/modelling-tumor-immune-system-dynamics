function f_q = simul_diff_quiescent(t,y)
a2=0.174e-1;
Cm=1e9;
b2=5.2e-8;
n2=2.8e-7;
s=1.3e4;
g=2.5e-2;
h=2.02e7;
p=1.8e-8;
f=4.12e-2;
r=1.2e-7;
j=10e-2;
k=2.02e7;
u=1.7e-12;
m=2e-2;

f_q(1,1)=a2*y(1)*log(Cm/(y(1)))-b2*y(2)*y(1)-n2*y(3)*y(1);
f_q(2,1)=s+y(2)*(g*(y(1)^2/(h+y(1)^2)))-p*y(1)*y(2)-f*y(2);
f_q(3,1)=r*y(2)*y(1)+y(3)*(j*(y(1)^2)/(k+y(1)^2))-u*y(1)*y(3)-m*y(3);
end