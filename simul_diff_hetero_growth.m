function f_b = simul_diff_hetero_growth(t,y)
a1=0.38e-1;
a2=0.174e-1;
Cm=1e9;
b1=1.56e-8;
b2=5.2e-8;
n1=0.21e-7;
n2=2.8e-7;
s=1.3e4;
g=2.5e-2;
h=2.02e7;
p=1.8e-8;
f_param=4.12e-2;
r1=0.36e-7;
r2=1.2e-7;
j1=10e-2;
k=2.02e7;
u1=2.1e-8;
u2=1.7e-12;
m=2e-2;
C=y(1)+y(2);

a1h=((a1*y(1))+(a2*y(2)))/(y(1)+y(2));
b1h=((b1*y(1))+(b2*y(2)))/(y(1)+y(2));
n1h=((n1*y(1))+(n2*y(2)))/(y(1)+y(2));
r1h=((r1*y(1))+(r2*y(2)))/(y(1)+y(2));
u1h=((u1*y(1))+(u2*y(2)))/(y(1)+y(2));

f_b(1,1)=(a1h)*y(1)*log(Cm/C)-(b1h)*y(3)*y(1)-(n1h)*y(4)*y(1);
f_b(2,1)=(a2)*y(2)*log(Cm/C)-(b2)*y(3)*y(2)-(n2)*y(4)*y(2);
f_b(3,1)=s+y(3)*(g*(C^2/(h+C^2)))-p*y(3)*C-(f_param)*y(3);
f_b(4,1)=(r1h)*y(3)*C+y(4)*(j1*(C^2/(k+C^2)))-(u1h)*C*y(4)-(m)*y(4);
end